  void Write(uint32_t rn)
  {
	 uint8_t write_data[4];
	 write_data[0] = (uint8_t)(rn >> 24);
	 write_data[1] = (uint8_t)(rn >> 16);
	 write_data[2] = (uint8_t)(rn >> 8);
	 write_data[3] = (uint8_t)(rn >> 0);
     HAL_I2C_Mem_Write(&hi2c1, I2C_ADDRESS, 0x0010, I2C_MEMADD_SIZE_16BIT, write_data, 4, HAL_MAX_DELAY);
  }
  uint32_t Read()
  {
	 uint8_t read_data[4];
     HAL_I2C_Mem_Read(&hi2c1, I2C_ADDRESS, 0x0010, I2C_MEMADD_SIZE_16BIT, read_data, 4, HAL_MAX_DELAY);
     uint32_t read_back_number = ((uint32_t)read_data[0] << 24 | (uint32_t)read_data[1] << 16 | (uint32_t)read_data[2] << 8  | (uint32_t)read_data[3] << 0);
     return read_back_number;
  }
  void Display_Number(uint32_t rn)
  {
	  itoa(rn, txt, 10);// Convert to string
	  for (int pos = (16-strlen(txt))/2; pos >= 0; pos--)
	  {
		Clear_Number();// Clear number
		if(pos == 0) pos = 16;//To the right of the LCD
	  	lcd_goto(pos, 1);// Position of the LCD
	  	lcd_puts(txt); // Display number
	  	HAL_Delay(300);//Delay 300ms
	  }
  }
